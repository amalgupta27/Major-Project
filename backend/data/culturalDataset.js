// Function to find matching cultural facts
export function findCulturalFact(question) {
  const normalizedQuestion = question.toLowerCase().trim();
  
  // First, try exact matches
  for (const fact of culturalDataset) {
    if (fact.question.toLowerCase() === normalizedQuestion) {
      return fact;
    }
  }
  
  // Then, try keyword matches
  for (const fact of culturalDataset) {
    const questionWords = normalizedQuestion.split(/\s+/);
    const keywordMatches = fact.keywords.filter(keyword => 
      questionWords.some(word => word.includes(keyword) || keyword.includes(word))
    );
    
    // If more than 50% of keywords match, consider it a match
    if (keywordMatches.length >= Math.ceil(fact.keywords.length * 0.5)) {
      return fact;
    }
  }
  
  // Finally, try partial matches
  for (const fact of culturalDataset) {
    if (fact.question.toLowerCase().includes(normalizedQuestion) || 
        normalizedQuestion.includes(fact.question.toLowerCase())) {
      return fact;
    }
  }
  
  return null;
}

